    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        
        try{
            int indice = 0;

            if (!txtUsuario.getText().isEmpty() && !txtPass.getText().isEmpty()) {

                    for (int i = 0; i < registro.listaClientes.size(); i++) {
                        if(txtUsuario.getText().equals(registro.listaClientes.get(i).getUsuario())){
                            indice = i;
                            break;
                        }

                    }

                        String usuario = registro.listaClientes.get(indice).usuario;
                        String contrase = registro.listaClientes.get(indice).pass;

                        if (usuario.equals(txtUsuario.getText()) && contrase.equals(txtPass.getText())){

                                user = txtUsuario.getText();

                                Operaciones operaciones = new Operaciones(registro, this);

                                txtUsuario.setText(null);
                                txtUsuario.requestFocus();
                                txtPass.setText(null);

                                JOptionPane.showMessageDialog(rootPane, "Bienvenido "+ user, "BANCO MERCANTIL", JOptionPane.DEFAULT_OPTION);

                                this.setVisible(false);
                                operaciones.setVisible(true);
                                operaciones.repaint();
                                operaciones.validate();

                        } else {
                            JOptionPane.showMessageDialog(rootPane, "USUARIO NO REGISTRADO", "ERROR", JOptionPane.ERROR_MESSAGE);
                            txtUsuario.setText(null);
                            txtPass.setText(null);
                            txtUsuario.requestFocus();
                        }

            } else {
                JOptionPane.showMessageDialog(this, "Los campos no pueden esta vacios", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } catch (IndexOutOfBoundsException e1){
            JOptionPane.showMessageDialog(rootPane, e1.getMessage()+"\n\n-----> USUARIO NO ENCONTRADO \n-----> DEBES REGISTRARTE !!!");
            txtUsuario.setText(null);
            txtPass.setText(null);
            txtUsuario.requestFocus();
        }
        
        
        
        
        
        
    }//GEN-LAST:event_btnEntrarActionPerformed

